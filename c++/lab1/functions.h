/**
 * \brief Gets the minimum of 2 integers.
 *
 * This function returns the minimum of the two inputs.
 * 
 * \param a The first integer.
 * \param b The second integer.
 * \return The minimum between a and b.
 */
long minimum (long a, long b); 

/**
 * \brief Gets the maximum of 2 integers.
 *
 * This function returns the maximum of the two inputs.
 * 
 * \param a The first integer.
 * \param b The second integer.
 * \return The maximum between a and b.
 */
long maximum (long a, long b); 

/**
 * \brief Gets the product of 2 integers.
 *
 * This function returns the product of the two inputs.
 * 
 * \param a The first integer.
 * \param b The second integer.
 * \return The product of a by b.
 */
long product (long a, long b); 

/**
 * \brief Gets the power of a number.
 *
 * This function returns the first input powered by the second input.
 * 
 * \param a The first integer.
 * \param b The second integer.
 * \return The power of a by b.
 */
long power (long a, long b); 

/**
 * \brief Gets GCD of 2 integers.
 *
 * This function returns the GCD of the two inputs.
 * 
 * \param a The first integer.
 * \param b The second integer.
 * \return The greatest common divisor.
 */
long gcd (long a, long b); 